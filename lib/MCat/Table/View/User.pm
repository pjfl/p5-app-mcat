package MCat::Table::View::User;

use HTML::StateTable::Constants qw( FALSE TRUE );
use Moo;

extends 'MCat::Table::View::Object';

has '+caption' => default => 'User View';

has '+form_buttons' => default => sub {
   my $self    = shift;
   my $context = $self->context;
   my $user_id = $self->result->id;

   return [{
      action    => $context->uri_for_action('user/edit', [$user_id]),
      method    => 'get',
      selection => 'disable_on_select',
      value     => 'Edit',
   },{
      action    => $context->uri_for_action('user/profile', [$user_id]),
      method    => 'get',
      selection => 'disable_on_select',
      value     => 'Profile',
   },{
      action    => $context->uri_for_action('user/delete', [$user_id]),
      classes   => 'right',
      selection => 'disable_on_select',
      value     => 'Delete',
   }];
};

sub BUILD {
   my $self    = shift;
   my $context = $self->context;

   push @{$self->add_columns}, '2FA Enabled' => {
      cell_traits => ['Bool'],
      value       => $self->result->enable_2fa,
   };
   push @{$self->add_columns}, 'Time Zone' => $context->time_zone;

   return;
}

use namespace::autoclean;

1;
